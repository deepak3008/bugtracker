package com.example.bugs.tracker;

public class BugTrackerResponse {
	
	private String id;
	
	private String issueUrl;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getIssueUrl() {
		return issueUrl;
	}

	public void setIssueUrl(String issueUrl) {
		this.issueUrl = issueUrl;
	}
	
	

}
